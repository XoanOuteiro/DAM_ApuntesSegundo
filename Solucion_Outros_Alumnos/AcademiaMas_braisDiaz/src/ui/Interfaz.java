    private void crearAlumno(String nombre,String localidad,ArrayList<Modulo> listaModulosAlumno){
        Alumno alumno1=new Alumno(nombre, localidad, listaModulosAlumno);
        listaAlumnos.add(alumno1);
    }
    
    private void crearModulo(Modulo modulo){
        listaModulos.add(modulo);
    }
    
    private void cargarComboBox(){
        try{
            for (Modulo modulo : listaModulos){
                jComboBoxModulos.addItem(modulo.getNombre());
            }
        }
        catch(NullPointerException e){
            e.getStackTrace();
        }
    }
    
    private void cargarList(String modulo){
        try{
            modeloLista.addElement(modulo);
            jListModulos.setModel(modeloLista);
        }
        catch(NullPointerException e){
            e.getStackTrace();
        }
    }
    
    private DefaultListModel cargarListModulosAlumno(Alumno alumno) {
        ArrayList<String> nombresModulos=new ArrayList<>();
        for (int cont=0; cont<alumno.getNombreModulos().length; cont++) {
            nombresModulos.add(alumno.getNombreModulos()[cont]);
        }
        modeloListaAlumno.addAll(nombresModulos);
        return modeloListaAlumno;
    }
    
    private void grabarModulosAlumno() {
        for (int cont=0; cont<modeloLista.size(); cont++) {
            for (int cont2=0; cont2<listaModulos.size(); cont2++) {
                if (modeloLista.get(cont).equals(listaModulos.get(cont2).getNombre())){
                    listaModulosAlumno.add(listaModulos.get(cont2));
                }
            }
        }
    }
    
    private void limpiarPanelEngadirAlumno(){
        jTextFieldNombreAlumno.setText("");
        jTextFieldLocalidad.setText("");
        modeloLista.clear();
        jListModulos.setModel(modeloLista);
        jComboBoxModulos.removeAllItems();
    }
    
