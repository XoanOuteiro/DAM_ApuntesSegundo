    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try{
            String nombreModulo=jTextFieldNombreModulo.getText();
            int unidades=Integer.parseInt(jTextFieldUnidadesModulo.getText());
            int horas=Integer.parseInt(jTextFieldHorasModulo.getText());
            Modulo modulo=new Modulo(unidades, nombreModulo, horas);
            crearModulo(modulo);
            JOptionPane.showMessageDialog(jDEngadirAlumno, "Módulo creado con éxito");
            jDEngadirModulo.setVisible(false);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(jDEngadirAlumno, "Unidades y Horas tienen que ser números");
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

